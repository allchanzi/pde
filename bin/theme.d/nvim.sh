write_nvim_theme() {
  cat > "$DOTFILES_DIR/nvim/lua/config/theme.lua" <<EOF
return {
  name = "$THEME_NAME",
  colorscheme = "$NVIM_COLORSCHEME",
  flavour = "$NVIM_FLAVOUR",
  background = "$NVIM_BACKGROUND",
  palette = {
    base = "$BASE",
    text = "$TEXT",
    subtle = "$SUBTLE",
    surface0 = "$SURFACE0",
    surface1 = "$SURFACE1",
    surface2 = "$SURFACE2",
    subtext0 = "$SUBTEXT0",
    subtext1 = "$SUBTEXT1",
    overlay1 = "$OVERLAY1",
    rosewater = "$ROSEWATER",
    lavender = "$LAVENDER",
    blue = "$BLUE",
    pink = "$PINK",
    green = "$GREEN",
    yellow = "$YELLOW",
    cyan = "$CYAN",
    red = "$RED",
    white = "$WHITE",
  },
  color_overrides = {
    all = {
      base = "$BASE",
      mantle = "$SURFACE0",
      crust = "$SURFACE1",
      text = "$TEXT",
      blue = "$BLUE",
      pink = "$PINK",
      green = "$GREEN",
      yellow = "$YELLOW",
      red = "$RED",
      teal = "$CYAN",
      lavender = "$BLUE",
    },
  },
  custom_highlights = {
    CursorLine = { bg = "$SURFACE0" },
    Visual = { bg = "$SURFACE1" },
    Search = { bg = "$YELLOW", fg = "$BASE" },
    IncSearch = { bg = "$PINK", fg = "$BASE" },
    NormalFloat = { bg = "$BASE" },
    FloatBorder = { fg = "$BLUE", bg = "$BASE" },
    WinSeparator = { fg = "$SURFACE1" },
    Pmenu = { bg = "$SURFACE0" },
    PmenuSel = { bg = "$SURFACE1", fg = "$TEXT" },
    TroubleNormal = { fg = "$TEXT", bg = "$BASE" },
    TroubleNormalNC = { fg = "$TEXT", bg = "$BASE" },
    TroubleText = { fg = "$TEXT", bg = "$BASE" },
    TroubleIndent = { fg = "$SUBTLE", bg = "$BASE" },
    TroublePos = { fg = "$SUBTLE", bg = "$BASE" },
    TroubleCount = { fg = "$YELLOW", bg = "$BASE" },
    TroubleSource = { fg = "$SUBTLE", bg = "$BASE" },
    TroubleFilename = { fg = "$BLUE", bg = "$BASE" },
    WhichKeyNormal = { fg = "$TEXT", bg = "$BASE" },
    WhichKeyBorder = { fg = "$BLUE", bg = "$BASE" },
    WhichKeyTitle = { fg = "$LAVENDER", bg = "$BASE", bold = true },
    WhichKey = { fg = "$YELLOW", bg = "$BASE", bold = true },
    WhichKeyDesc = { fg = "$TEXT", bg = "$BASE" },
    WhichKeyGroup = { fg = "$BLUE", bg = "$BASE", bold = true },
    WhichKeySeparator = { fg = "$SUBTLE", bg = "$BASE" },
    WhichKeyValue = { fg = "$SUBTLE", bg = "$BASE" },
    NvimTreeNormal = { bg = "$BASE" },
    NvimTreeNormalNC = { bg = "$BASE" },
    NvimTreeEndOfBuffer = { bg = "$BASE" },
    NvimTreeWinSeparator = { fg = "$SURFACE1", bg = "$BASE" },
  },
}
EOF
}

